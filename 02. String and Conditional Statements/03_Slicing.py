# Description: This program demonstrates string slicing in Python.

str = "Kiran Sarwar"
print(str[0:5])  # Output: Kiran
print(str[6:])   # Output: Sarwar

# Negative indexing
print(str[-5:-1])  # Output: Sarw
# Last character is not included in the output
